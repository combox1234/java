package methods;

public class asmd {
    private double a,b;
    public asmd(double x,double y){
        this.a = x;
        this.b = y;
    }
    public double add(){
        return a+b;
    }
    public double sub(){
        return a-b;
    }
    public double mul(){
        return a*b;
    }
    public double div(){
        return a/b;
    }

}
